# Cálcular la distancia entre 2 planeas: Tierra y Jupiter
# Tierra: 149,597,870
# Jupiter: 778,547,200
first_planet = 149597870
second_planet = 778547200

distance_km = second_planet - first_planet
print(distance_km)
# Conversión de la distancia en millas
distance_mi = distance_km / 1.609344
print(distance_mi)