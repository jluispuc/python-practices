#Use una instrucción if si quiere ejecutar código solo si se cumple una condición concreta. 
#La sintaxis de una instrucción if siempre es la siguiente:
a = 93
b = 27
if a >= b:
    print(a)
print(b)
#Nota: En Python, None y 0 también se interpretan como False.
#Nota: En Python, se debe aplicar sangría al cuerpo de una instrucción if.